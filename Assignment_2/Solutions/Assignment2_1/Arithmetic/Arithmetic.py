#Module named as Arithmetic which contains 4 functions as Add() for addition, Sub()
#for subtraction, Mult() for multiplication and Div() for division.

#Addition Function
def Addition(no1,no2):
    ans = no1 + no2
    return ans

#Substraction Function
def Substraction(no1,no2):
    ans = no1 - no2
    return ans

#Multiplication Function
def Multiplication(no1,no2):
	ans = no1 * no2
	return ans

#Division Function
def Division(no1,no2):
	ans = no1 / no2
	return ans

